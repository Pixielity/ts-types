/**
 * Parameter type enum
 */
export enum ParamType {
  /**
   * Route parameter
   */
  PARAM = "param",

  /**
   * Query parameter
   */
  QUERY = "query",

  /**
   * Request body
   */
  BODY = "body",

  /**
   * Request object
   */
  REQUEST = "request",

  /**
   * Response object
   */
  RESPONSE = "response",
}
